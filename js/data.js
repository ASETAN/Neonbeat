// JSONP Helper to bypass CORS with timeout
function jsonp(url, timeout = 10000) {
    return new Promise((resolve, reject) => {
        const callbackName = 'jsonp_' + Math.round(100000 * Math.random());
        const timeoutId = setTimeout(() => {
            cleanup();
            reject(new Error('JSONP request timed out'));
        }, timeout);

        function cleanup() {
            clearTimeout(timeoutId);
            if (window[callbackName]) delete window[callbackName];
            if (script.parentNode) document.body.removeChild(script);
        }

        window[callbackName] = function (data) {
            cleanup();
            resolve(data);
        };

        const script = document.createElement('script');
        script.src = url + (url.indexOf('?') >= 0 ? '&' : '?') + 'callback=' + callbackName;
        script.onerror = () => {
            cleanup();
            reject(new Error('JSONP request failed'));
        };
        document.body.appendChild(script);
    });
}

async function fetchArtistReleases(artist) {
    try {
        if (!artist.itunesId) {
            console.warn(`Skipping releases for ${artist.name} (No ID)`);
            return [];
        }

        // Use API Proxy if available (Vercel or Cloudflare Pages)
        const isApiSupported = window.location.hostname.includes('vercel.app') ||
            window.location.hostname.includes('pages.dev') ||
            window.location.hostname.includes('workers.dev') ||
            window.location.hostname.includes('localhost') ||
            window.location.hostname.includes('127.0.0.1');
        let data;

        if (isApiSupported) {
            // Use Vercel API Proxy
            // Increased limit to 200 to get more history. 
            // entity=album includes albums, EPs, and singles (collection types).
            const url = `/api/lookup?id=${artist.itunesId}&entity=album&limit=200&sort=recent&country=JP`;
            const response = await fetch(url);
            if (!response.ok) throw new Error('API Network response was not ok');
            data = await response.json();
        } else {
            // Use JSONP locally
            const url = `https://itunes.apple.com/lookup?id=${artist.itunesId}&entity=album&limit=200&sort=recent&country=JP`;
            data = await jsonp(url);
        }

        if (!data.results || data.results.length === 0) return [];

        // Filter and Map
        // Results[0] is Artist, rest are Albums
        const releases = data.results
            .filter(item => item.wrapperType === 'collection')
            .map(item => ({
                id: String(item.collectionId),
                artistId: artist.id,
                title: item.collectionName,
                date: item.releaseDate, // ISO String
                type: item.collectionType === 'Album' && item.trackCount < 7 ? 'EP/Single' : 'Album',
                image: item.artworkUrl100.replace('100x100', '600x600'), // Upgrade quality
                links: {
                    apple: item.collectionViewUrl,
                    youtube: `https://music.youtube.com/search?q=${encodeURIComponent(item.collectionName + ' ' + item.artistName)}`,
                    spotify: `https://open.spotify.com/search/${encodeURIComponent(item.collectionName)}`,
                    amazon: `https://music.amazon.com/search/${encodeURIComponent(item.collectionName + ' ' + item.artistName)}`
                }
            }));

        // Sort by Date - Remove slice to show all history
        return releases.sort((a, b) => new Date(b.date) - new Date(a.date));
    } catch (error) {
        console.error(`Failed to fetch releases for ${artist.name}:`, error);
        return [];
    }
}

async function fetchAlbumDetails(collectionId) {
    try {
        const isApiSupported = window.location.hostname.includes('vercel.app') ||
            window.location.hostname.includes('pages.dev') ||
            window.location.hostname.includes('workers.dev') ||
            window.location.hostname.includes('localhost') ||
            window.location.hostname.includes('127.0.0.1');
        let data;

        // Strategy 1: Direct Lookup
        if (isApiSupported) {
            const url = `/api/lookup?id=${collectionId}&entity=song&country=JP`;
            const response = await fetch(url);
            if (!response.ok) throw new Error('API Network response was not ok');
            data = await response.json();
        } else {
            const url = `https://itunes.apple.com/lookup?id=${collectionId}&entity=song&country=JP`;
            data = await jsonp(url);
        }

        // Strategy 2: Fallback to Search if Lookup returns only the collection but no tracks
        // (Fixes "The Feels" issue where lookup returns 1 item (collection) but no songs)
        if (!data.results || data.results.length <= 1) {
            console.warn(`Lookup for ${collectionId} returned insufficient data. Trying fallback search...`);

            // We need the collection name to search. If we have result[0], use it.
            let collectionName = "";
            let artistName = "";

            if (data.results && data.results.length === 1) {
                collectionName = data.results[0].collectionName;
                artistName = data.results[0].artistName;
            } else {
                // If we don't even have the collection, we can't search by name easily without passing it in.
                // But usually we at least get the collection wrapper.
                // If completely empty, we might fail unless we fetch release info differently.
                // For now, assume we have the collection object.
                return null;
            }

            // Cleanup name for search (remove - EP, - Single etc to be broader, or keep specific?)
            // Keep specific first.
            // Use attribute=albumTerm to be precise
            const searchTerm = encodeURIComponent(collectionName);
            const searchUrl = isApiSupported
                ? `/api/search?term=${searchTerm}&attribute=albumTerm&entity=song&country=JP&limit=50`
                : `https://itunes.apple.com/search?term=${searchTerm}&attribute=albumTerm&entity=song&country=JP&limit=50`;

            let searchData;
            if (isApiSupported) {
                const res = await fetch(searchUrl);
                searchData = await res.json();
            } else {
                searchData = await jsonp(searchUrl);
            }

            if (searchData.results && searchData.results.length > 0) {
                // Filter results to match the specific collectionId if possible, or matches exact name
                // Some tracks might have different collectionIDs (e.g. from compilation), so we prioritize strict match
                const matchingTracks = searchData.results.filter(t =>
                    String(t.collectionId) === String(collectionId) && t.wrapperType === 'track'
                );

                if (matchingTracks.length > 0) {
                    // We found tracks! Reconstruct data structure.
                    // We need the collection object as [0]
                    data = { results: [data.results[0], ...matchingTracks] };
                } else {
                    // If strict ID match fails, maybe lenient match on name?
                    // Risk of mixing albums. Let's stick to ID match or strictly same Artist + Album Name
                    const nameMatchTracks = searchData.results.filter(t =>
                        t.collectionName === collectionName && t.artistName === artistName && t.wrapperType === 'track'
                    );
                    if (nameMatchTracks.length > 0) {
                        data = { results: [data.results[0], ...nameMatchTracks] };
                    }
                }
            }
        }

        if (!data.results || data.results.length === 0) return null;

        const collection = data.results[0];
        // Sort tracks just in case fallback search returned them out of order
        const tracks = data.results.slice(1).sort((a, b) => a.trackNumber - b.trackNumber);

        return {
            collection: collection,
            tracks: tracks.map(track => ({
                trackNumber: track.trackNumber,
                title: track.trackName,
                durationMs: track.trackTimeMillis,
                previewUrl: track.previewUrl,
                artist: track.artistName
            }))
        };
    } catch (error) {
        console.error(`Failed to fetch details for album ${collectionId}:`, error);
        return null; // Don't break the UI, just show error in modal
    }
}

